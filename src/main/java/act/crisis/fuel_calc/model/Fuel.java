package act.crisis.fuel_calc.model;

public class Fuel {
    private String name;
    private Float price;

    public Fuel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getPrice() {
        return price;
    }

    public void setPrice(Float price) {
        this.price = price;
    }
}
